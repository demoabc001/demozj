public class DmUser {
    private int urId;
    private String urUserName;
    private String urPassword;
    private String urName;
    private String urSex;
    private int urAge;
	public int getUrId() {
		return urId;
	}
	public void setUrId(int urId) {
		this.urId = urId;
	}
	public String getUrUserName() {
		return urUserName;
	}
	public void setUrUserName(String urUserName) {
		this.urUserName = urUserName;
	}
	public String getUrPassword() {
		return urPassword;
	}
	public void setUrPassword(String urPassword) {
		this.urPassword = urPassword;
	}
	public String getUrName() {
		return urName;
	}
	public void setUrName(String urName) {
		this.urName = urName;
	}
	public String getUrSex() {
		return urSex;
	}
	public void setUrSex(String urSex) {
		this.urSex = urSex;
	}
	public int getUrAge() {
		return urAge;
	}
	public void setUrAge(int urAge) {
		this.urAge = urAge;
	}
    
    
}
