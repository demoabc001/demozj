package cn.com.demo.entity;

public class DmMobileInfo {
    private int mbId;
    private String mbName;
    private int mbMemory;
    private String mbColor;
    private double mbPrice;
	public int getMbId() {
		return mbId;
	}
	public void setMbId(int mbId) {
		this.mbId = mbId;
	}
	public String getMbName() {
		return mbName;
	}
	public void setMbName(String mbName) {
		this.mbName = mbName;
	}
	public int getMbMemory() {
		return mbMemory;
	}
	public void setMbMemory(int mbMemory) {
		this.mbMemory = mbMemory;
	}
	public String getMbColor() {
		return mbColor;
	}
	public void setMbColor(String mbColor) {
		this.mbColor = mbColor;
	}
	public double getMbPrice() {
		return mbPrice;
	}
	public void setMbPrice(double mbPrice) {
		this.mbPrice = mbPrice;
	}
}
